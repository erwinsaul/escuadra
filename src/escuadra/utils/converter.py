def celsius_to_fahrenheit(celsius: float) -> float:
    """
    Convierte grados Celsius a Fahrenheit.

    Ejemplo:
        >>> celsius_to_fahrenheit(80)
        212.0
    """
    return (celsius * 9 / 5) + 32


def km_to_miles(km: float) -> float:
    """
    Convierte kilómetros a millas.

    Ejemplo:
        >>> km_to_miles(4)
        2.485484
    """
    return km * 0.621371

